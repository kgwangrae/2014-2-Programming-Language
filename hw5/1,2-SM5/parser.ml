let rec desugarLet: declLet list * K.K.exp -> K.K.exp  =
  fun (l, e) ->
  	match l with
	  [] -> raise EmptyBinding
	| a::[] ->
		(match a with
          Val(x, e') -> K.K.LETV(x,e',e)
      	| Fun(f,x,e') -> K.K.LETF(f,x,e',e))
   | a::r -> 
     (match a with
        Val(x, e') -> K.K.LETV(x,e', desugarLet(r,e))
      | Fun(f,x,e') -> K.K.LETF(f,x,e', desugarLet(r,e)))

